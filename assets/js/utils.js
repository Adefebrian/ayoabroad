/**
 * Calculate price bar width as percentage of global max
 * @param {number} price
 * @param {number} globalMax
 * @returns {number}
 */
function getPriceBarWidth(price, globalMax) {
  if (price === 0 || globalMax === 0) return 5;
  return Math.max(5, (price / globalMax) * 100);
}

/**
 * Debounce function for search input
 * @param {function} func
 * @param {number} wait
 * @returns {function}
 */
function debounce(func, wait = 300) {
  let timeout;
  return function executedFunction(...args) {
    clearTimeout(timeout);
    timeout = setTimeout(() => func.apply(this, args), wait);
  };
}

/**
 * Animate counting up a number
 * @param {HTMLElement} el
 * @param {number} target
 * @param {number} duration
 * @param {string} suffix
 */
function animateCounter(el, target, duration = 1500, suffix = '') {
  let start = 0;
  const startTime = performance.now();

  function update(currentTime) {
    const elapsed = currentTime - startTime;
    const progress = Math.min(elapsed / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    const current = Math.floor(eased * target);

    el.textContent = current + suffix;

    if (progress < 1) {
      requestAnimationFrame(update);
    } else {
      el.textContent = target + suffix;
    }
  }

  requestAnimationFrame(update);
}

/**
 * Check if element is in viewport
 * @param {HTMLElement} el
 * @param {number} offset
 * @returns {boolean}
 */
function isInViewport(el, offset = 0) {
  const rect = el.getBoundingClientRect();
  return rect.top <= (window.innerHeight - offset);
}

