/**
 * Format number to Indonesian Rupiah string
 * @param {number} amount
 * @returns {string}
 */
function formatRupiah(amount) {
  if (amount === 0) return 'Gratis / By Request';
  if (amount < 1000000) {
    return 'Rp ' + (amount / 1000).toLocaleString('id-ID') + ' rb';
  }
  const jt = amount / 1000000;
  if (Number.isInteger(jt)) {
    return 'Rp ' + jt.toLocaleString('id-ID') + ' Jt';
  }
  return 'Rp ' + jt.toLocaleString('id-ID', { maximumFractionDigits: 1 }) + ' Jt';
}

/**
 * Format number with Indonesian separator (dots)
 * @param {number} num
 * @returns {string}
 */
function formatNumber(num) {
  return num.toLocaleString('id-ID');
}

/**
 * Calculate average price for a competitor
 * @param {object} comp
 * @returns {number}
 */
function getAvgPrice(comp) {
  if (comp.price_min === 0 && comp.price_max === 0) return 0;
  if (comp.price_min === 0) return comp.price_max;
  if (comp.price_max === 0) return comp.price_min;
  return (comp.price_min + comp.price_max) / 2;
}

